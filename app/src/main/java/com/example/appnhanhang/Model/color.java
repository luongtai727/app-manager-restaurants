package com.example.appnhanhang.Model;

public class color {
    int R;
    int G;
    int B;

    public color(int r, int g, int b) {
        R = r;
        G = g;
        B = b;
    }

    public int getR() {
        return R;
    }

    public int getG() {
        return G;
    }

    public int getB() {
        return B;
    }
}
